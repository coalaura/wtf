func DetectPYC(b types.Buffer) *types.Metadata {
	if b.Len() < 4 {
		return nil
	}

	if b[2] != 0x0d || b[3] != 0x0a {
		return nil
	}

	magic, ok := b.U16LE(0)
	if !ok {
		return nil
	}

	if _, found := pycMagic[magic]; !found {
		return nil
	}

	return &types.Metadata{
		Kind: types.KindPythonBytecode,
	}
}
